package week1.day1;

public class LearnDatatypesAndVariables {

	public static void main(String[] args) {
		String brandName = "Android";
		char color = 'S';
		int price = 20000;
		String model = "Note pro 8";
		byte memory = 120;
		float screenSize = 20.5f;
		boolean isTouchscreen = true;
		long phoneNumber = 919265842504L;
		System.out.println("Brand Name :" + brandName);
		System.out.println("Color :" + color);
		System.out.println("Price :" + price);
		System.out.println("Model Name :" + model);
		System.out.println("Memory :" + memory);
		System.out.println("Screen Size :" + screenSize);
		System.out.println("Is Touch Screen :" + isTouchscreen);
		System.out.println("Phone Number :" + phoneNumber);
		System.out.println("My Mobile Brandname is " + brandName + ",Color is " + color + ",Price is " + price + ",Model Name is :" + model + "");

	}

}
